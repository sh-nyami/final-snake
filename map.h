    WINDOW* gameWindow;

public:
    Map()
    {
        initscr();
        resize_term(40, 100);
        start_color();

        init_pair(10, COLOR_WHITE, COLOR_CYAN);
        init_pair(0, COLOR_BLACK, COLOR_WHITE);
        init_pair(1, COLOR_BLACK, COLOR_GRAY);    // wall
        init_pair(2, COLOR_BLACK, COLOR_BLACK);   // immune wall
        init_pair(3, COLOR_BLACK, COLOR_MAGENTA);  // snake head
        init_pair(4, COLOR_BLACK, COlOR_WYELLOW); // snake body
        init_pair(5, COLOR_BLACK, COLOR_GREEN);   // add item
        init_pair(6, COLOR_BLACK, COLOR_RED);     // minus item
        init_pair(7, COLOR_BLACK, COLOR_YELLOW); // gate
        bkgd(COLOR_PAIR(10));
        attron(COLOR_PAIR(10));
        border('.', '.', '.', '.', '.', '.', '.', '.');
        attroff(COLOR_PAIR(10));
        gameWindow = newwin(21, 21, 5, 5);
        nodelay(stdscr, TRUE);
        nodelay(gameWindow, TRUE);
        init();
        refresh();
        wrefresh(gameWindow);
    }
    ~Map()
    {
        endwin();
    }
    void init()
    {
        for (int i = 0; i < 21; i++) {
            for (int j = 0; j < 21; j++)
            {
                ingameMap[i][j] = map[stage][i][j];
                wattron(gameWindow, COLOR_PAIR(ingameMap[i][j]));
                mvwprintw(gameWindow, i, j, " ");
                wattroff(gameWindow, COLOR_PAIR(ingameMap[i][j]));
            }
        }
    }
    void view(Snake s, Item item[], Gate g, Score sc)
    {
        attron(COLOR_PAIR(0));
        mvprintw(5, 30, "Score Board");
        mvprintw(6, 30, "Body: %d / %d", sc.bodyScore, sc.maxbodyScore);
        mvprintw(7, 30, "Growth: %d", sc.growthScore);
        mvprintw(8, 30, "Poison: %d", sc.poisonScore);
        mvprintw(9, 30, "Gate: %d", sc.gateScore);

        mvprintw(11, 30, "Misson");
        mvprintw(12, 30, "Maxbody: %d (%s)", sc.bodyScore_mission, sc.maxbodyScore >= sc.bodyScore_mission ? "v" : " ");
        mvprintw(13, 30, "Growth: %d (%s)", sc.growthScore_mission, sc.growthScore >= sc.growthScore_mission ? "v" : " ");
        mvprintw(14, 30, "Poison: %d (%s)", sc.poisonScore_mission, sc.poisonScore >= sc.poisonScore_mission ? "v" : " ");
        mvprintw(15, 30, "Gate: %d (%s)", sc.gateScore_mission, sc.gateScore >= sc.gateScore_mission ? "v" : " ");
        attroff(COLOR_PAIR(0));
        keypad(stdscr, TRUE);
        curs_set(0);
        noecho();
        for (int i = 0; i < 21; i++)
            for (int j = 0; j < 21; j++)
            {
                wattron(gameWindow, COLOR_PAIR(ingameMap[i][j]));
                mvwprintw(gameWindow, i, j, " ");
                wattroff(gameWindow, COLOR_PAIR(ingameMap[i][j]));
            }
        for (int i = 0; i < s.getLength(); i++)
        {
            wattron(gameWindow, COLOR_PAIR(4));
            mvwprintw(gameWindow, s.getBody(i).second, s.getBody(i).first, " ");
            wattroff(gameWindow, COLOR_PAIR(4));
        }
        if (g.getActive())
        {
            wattron(gameWindow, COLOR_PAIR(7));
            mvwprintw(gameWindow, g.getIn().second, g.getIn().first, " ");
            mvwprintw(gameWindow, g.getOut().second, g.getOut().first, " ");
            wattroff(gameWindow, COLOR_PAIR(7));
        }
        for (int i = 0; i < 3; i++)
        {
            if (item[i].getActive())
            {
                if (item[i].getValue() > 0)
                {
                    wattron(gameWindow, COLOR_PAIR(5));
                }
                else
                {
                    wattron(gameWindow, COLOR_PAIR(6));
                }
                mvwprintw(gameWindow, item[i].getY(), item[i].getX(), " ");
                if (item[i].getValue() > 0)
                {
                    wattroff(gameWindow, COLOR_PAIR(5));
                }
                else
                {
                    wattroff(gameWindow, COLOR_PAIR(6));
                }
            }
        }
        wattron(gameWindow, COLOR_PAIR(3));
        mvwprintw(gameWindow, s.getY(), s.getX(), " ");
        wattroff(gameWindow, COLOR_PAIR(3));

        wrefresh(gameWindow);
        usleep(tick);
    }
    void getTick(int n) {
        tick = n;
    }

    int isGameOver(Snake s, Score sc)
    {
        if (sc.maxbodyScore >= sc.bodyScore_mission && sc.growthScore >= sc.growthScore_mission
            && sc.poisonScore >= sc.poisonScore_mission && sc.gateScore >= sc.gateScore_mission)
        {
            attron(COLOR_PAIR(0));
            mvprintw(15, 10, "Completed!!");
            refresh();
            nodelay(stdscr, FALSE);
            nodelay(gameWindow, FALSE);
            getch();
            return 0;
        }
        if (ingameMap[s.getY()][s.getX()] == 1 || ingameMap[s.getY()][s.getX()] == 2)
        {
            attron(COLOR_PAIR(0));
            mvprintw(15, 12, "Oops...");
            refresh();
            nodelay(stdscr, FALSE);
            nodelay(gameWindow, FALSE);
            getch();
            return 1;
        }
        for (int i = 0; i < s.getLength(); i++)
        {
            if (s.getX() == s.getBody(i).first && s.getY() == s.getBody(i).second)
            {
                attron(COLOR_PAIR(0));
                mvprintw(15, 12, "Oops...");
                refresh();
                nodelay(stdscr, FALSE);
                nodelay(gameWindow, FALSE);
                getch();
                return 1;
            }
        }
        if (s.getLength() < 2)
        {
            attron(COLOR_PAIR(0));
            mvprintw(15, 12, "Oops...");
            refresh();
            nodelay(stdscr, FALSE);
            nodelay(gameWindow, FALSE);
            getch();
            return 1;
        }

        return 2;
    }
    mapArr getMap()
    {
        return ingameMap;
    }
